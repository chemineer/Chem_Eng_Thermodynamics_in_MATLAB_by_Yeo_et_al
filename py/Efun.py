import numpy as np

def Efun(T, P):
    """
    주어진 온도(T)와 압력(P)에서의 에너지 균형 함수값 계산 (파이썬 변환)
    """
    TR = 298.15
    
    # Ka 및 M 계산
    Ka = 742.91 * np.exp(5525.6 * (1.0 / T - 1.0 / 298.15))
    M = np.sqrt(27) * P * Ka / 4.0
    
    # eta 및 ni 계산
    eta = 1.0 - np.sqrt(1.0 - M / (1.0 + M))
    n1 = 0.5 - eta * 0.5
    n2 = 1.5 - eta * 1.5
    n3 = eta
    
    # Cp 적분항 계산 (s1, s2, s3)
    s1 = n1 * (31.2 * (T - TR) - 0.0136 * (T**2 - TR**2) / 2.0 + 
               2.68e-5 * (T**3 - TR**3) / 3.0 - 1.17e-8 * (T**4 - TR**4) / 4.0)
    
    s2 = n2 * (27.1 * (T - TR) + 0.00927 * (T**2 - TR**2) / 2.0 - 
               1.38e-5 * (T**3 - TR**3) / 3.0 + 7.65e-9 * (T**4 - TR**4) / 4.0)
    
    s3 = n3 * (27.3 * (T - TR) + 0.0238 * (T**2 - TR**2) / 2.0 + 
               1.71e-5 * (T**3 - TR**3) / 3.0 - 1.19e-8 * (T**4 - TR**4) / 4.0)
    
    # 에너지 함수값 f 계산[cite: 13]
    f = -(s1 + s2 + s3) + 45940.0 * eta
    
    return f